// propriedade length - retorna a quantidade de caracteres
console.log('Diego Baena'.length) // retorna o valor da string
console.log('Diego Baena'.charAt(4)) // retorna a letra contido no índice informado
console.log('Diego Baena'.indexOf('a')) // retorna o valor index da string em sua primeira ocorrencia
console.log('Diego Baena'.replace('Baena', 'Fronteira')) // substitui textos dentro de uma string
console.log('Diego Baena'.substr(7,9))// extrai parte de uma string
console.log('Diego Baena'.toLowerCase())
console.log('Diego Baena'.toUpperCase())
console.log('Diego Baena'.trim())//remove os espaços em branco das extremidades